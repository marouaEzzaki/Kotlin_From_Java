fun main() {

    /**
     * The do-while loop in Kotlin works the same as in Java.
     * It's used when you want to execute a block of code
     * at least once, and then repeatedly as long as the
     * condition in the while statement remains true.
     *
     * In contrast to a standard while loop, a do-while loop
     * will execute the code block before checking the condition,
     * which guarantees that the code block runs at least once.
     */

    do {
        println("hello") // This will print "hello" at least once, even if the condition is false.
    } while (true)

}